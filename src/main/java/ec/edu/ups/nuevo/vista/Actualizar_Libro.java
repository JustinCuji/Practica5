    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        String titulo = txtTitulo.getText();
        String autor = txtAutor.getText();
        String anioStr = txtAnio.getText();

        if (!titulo.isEmpty() && !autor.isEmpty() && !anioStr.isEmpty()) {
            try {
                int anio = Integer.parseInt(anioStr);

                Libro libroActualizado = new Libro(titulo, autor, anio);
                libroActualizado.setDisponible(Boolean.parseBoolean(txtDisponible.getText()));

                libroControlador.actualizarLibro(libroActualizado);
                JOptionPane.showMessageDialog(this, "Libro actualizado correctamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);

                // Limpia los campos después de la actualización
                limpiarCampos();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Ingrese un valor válido para el año", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Complete todos los campos antes de actualizar", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

