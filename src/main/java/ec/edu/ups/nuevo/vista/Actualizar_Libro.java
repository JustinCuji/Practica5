    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String titulo = txtTitulo.getText();

        if (!titulo.isEmpty()) {
            Libro libroEncontrado = libroControlador.buscar(titulo);

            if (libroEncontrado != null) {
                // Si el libro es encontrado, actualiza los campos en la interfaz
                txtAutor.setText(libroEncontrado.getAutor());
                txtAnio.setText(String.valueOf(libroEncontrado.getAño()));
                txtDisponible.setText(String.valueOf(libroEncontrado.isDisponible()));
            } else {
                // Si el libro no es encontrado, muestra un mensaje de error
                JOptionPane.showMessageDialog(this, "Libro no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Si el campo de título está vacío, muestra un mensaje de advertencia
            JOptionPane.showMessageDialog(this, "Ingrese el título del libro a buscar", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

